    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    double radio = Math.round(Double.valueOf(jTextFieldRadio.getText()));
    
    double perimetro = 2 * Math.PI * radio;
    
    int posicionSlider = jSlider1.getValue();
    jTextFieldDecimales.setText(String.valueOf(posicionSlider));
    
    NumberFormat formato = NumberFormat.getNumberInstance();
    formato.setMaximumFractionDigits(posicionSlider);
    String formatoDecimales = formato.format(perimetro);
    
    jTextFieldResultado.setEditable(false);
    
    Color colorFondo = new Color(255, 255, 0);
    
    jTextFieldResultado.setBackground(colorFondo);
    jTextFieldResultado.setText(formatoDecimales);
            
    }//GEN-LAST:event_jButton1ActionPerformed

